package edu.upc.dsa.orm.models;


public class Item {     //classe per objectes DINS del inventari    (objectes comprats a la botiga o conseguits al joc)

    private int id; //Asignamos un id al name creado
    private String name;
    private int price;
    private String description;
    private String image;

    public Item() {


    }

    public Item(int id, String name, int price, String description, String image) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.description = description;
        this.image = image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
